"""
Realice un programa que enumere los paises de la siguiente lista.
paises = ['Canada', 'USA', 'Mexico', 'Australia']
"""

# Lista de paises
paises = ['Canada', 'USA', 'Mexico', 'Australia', 'Republica Dominicana']
# Variable numerica
num = 0
# Ciclo for
for i in paises:
    # Variable numerica incrementandose
    num += 1
    # Se imprime la variable y el ciclo for.
    print(f'{num}: {i}')

